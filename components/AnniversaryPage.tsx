function AnniversaryItem({
  item,
  onEdit,
  onDelete,
  theme,
  t,
  language,
}: {
  item: CustomAnniversary;
  onEdit: (item: CustomAnniversary) => void;
  onDelete: (id: string) => void;
  theme: any;
  t: (key: string) => string;
  language: string;
}) {
  const days = daysFromToday(item.date);
  const isToday = days === 0;
  const { text: countdownText, isPast } = formatCountdown(days, t);

  const [celebrationImageUri, setCelebrationImageUri] = useState<string | null>(null);
  const [imageLoading, setImageLoading] = useState(false);

  useEffect(() => {
    if (!isToday) return;
    let cancelled = false;
    setImageLoading(true);
    getCelebrationImage(item.title, language)
      .then((uri) => {
        if (!cancelled) setCelebrationImageUri(uri);
      })
      .finally(() => {
        if (!cancelled) setImageLoading(false);
      });
    return () => { cancelled = true; };
  }, [isToday, item.title, language]);

  return (
    <TouchableOpacity
      style={[
        styles.itemCard,
        { backgroundColor: theme.colorCard, borderColor: theme.colorBorder },
        isToday && { flexDirection: "column" as const },
      ]}
      onPress={() => onEdit(item)}
      activeOpacity={0.7}
    >
      {isToday && (
        <View style={styles.itemCelebrationBanner}>
          {celebrationImageUri ? (
            <Image
              source={{ uri: celebrationImageUri }}
              style={styles.itemCelebrationImage}
              resizeMode="cover"
            />
          ) : (
            <View
              style={[
                styles.itemCelebrationPlaceholder,
                { backgroundColor: theme.colorPrimary + "20" },
              ]}
            >
              {imageLoading && (
                <ActivityIndicator size="small" color={theme.colorPrimary} />
              )}
            </View>
          )}
        </View>
      )}
      <View style={isToday ? styles.itemRowWrap : { flexDirection: "row" as const, alignItems: "center" as const, flex: 1 }}>
        <View style={styles.itemContent}>
          <Text style={[styles.itemTitle, { color: theme.colorForeground }]}>
            {item.title}
          </Text>
          <Text style={[styles.itemDate, { color: theme.colorMutedForeground }]}>
            {item.date}
          </Text>
          {item.reminder ? (
            <Text
              style={[styles.itemReminder, { color: theme.colorMutedForeground }]}
              numberOfLines={1}
            >
              {item.reminder}
            </Text>
          ) : null}
        </View>
        <View style={styles.itemRight}>
          <Text
            style={[
              styles.itemCountdown,
              {
                color: isPast
                  ? theme.colorMutedForeground
                  : theme.colorPrimary,
              },
            ]}
          >
            {countdownText}
          </Text>
          <View style={styles.itemActions}>
            <TouchableOpacity
              onPress={() => onEdit(item)}
              style={styles.actionIcon}
              hitSlop={{ top: 8, bottom: 8, left: 8, right: 8 }}
            >
              <Pencil size={15} color={theme.colorPrimary} />
            </TouchableOpacity>
            <TouchableOpacity
              onPress={() => onDelete(item.id)}
              style={styles.actionIcon}
              hitSlop={{ top: 8, bottom: 8, left: 8, right: 8 }}
            >
              <Trash2 size={15} color={theme.colorDestructive} />
            </TouchableOpacity>
          </View>
        </View>
      </View>
    </TouchableOpacity>
  );
}

