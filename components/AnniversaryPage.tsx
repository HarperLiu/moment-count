export function AnniversaryPage({
  onBack,
  relationshipStartDate,
  userId,
  linkedUser,
}: {
  onBack: () => void;
  relationshipStartDate: Date | null;
  userId: string;
  linkedUser: string | null;
}) {
  const { theme } = useThemeContext();
  const { t, language } = useLanguageContext();
  const [anniversaries, setAnniversaries] = useState<CustomAnniversary[]>([]);
  const [loading, setLoading] = useState(true);

  // Form state — used for both add and edit
  const [formVisible, setFormVisible] = useState(false);
  const [editingId, setEditingId] = useState<string | null>(null); // null = add mode
  const [formTitle, setFormTitle] = useState("");
  const [formDate, setFormDate] = useState(new Date());
  const [showDatePicker, setShowDatePicker] = useState(false);
  const [formReminder, setFormReminder] = useState("");
  const [formReminderDays, setFormReminderDays] = useState(0);
  const [reminderAutoGenerated, setReminderAutoGenerated] = useState(false);
  const [reminderLoading, setReminderLoading] = useState(false);

  const loadAnniversaries = useCallback(async () => {
    if (!userId) return;
    try {
      setLoading(true);
      const serverList = await api.getAnniversaries(userId);
      setAnniversaries(
        serverList.map((s) => ({
          id: s.id,
          title: s.title,
          date: s.date,
          reminder: s.reminder_text || undefined,
          reminderDaysBefore: s.reminder_days_before,
        }))
      );
    } catch (e) {
      console.warn("Failed to load anniversaries:", e);
    } finally {
      setLoading(false);
    }
  }, [userId]);

  useEffect(() => {
    loadAnniversaries();
  }, [loadAnniversaries]);

  const handleTitleChange = (text: string) => {
    setFormTitle(text);
  };

  // Auto-generate reminder via AI when title changes (debounced, only when user stops typing)
  useEffect(() => {
    const title = formTitle.trim();
    if (title.length < 2) return;
    if (formReminder && !reminderAutoGenerated) return; // user manually edited

    const timer = setTimeout(async () => {
      try {
        setReminderLoading(true);
        const result = await api.generateReminder({
          title,
          lang: language,
        });
        setFormReminder(result);
        setReminderAutoGenerated(true);
      } catch {
        if (!formReminder) {
          setFormReminder(autoReminder(title, t));
          setReminderAutoGenerated(true);
        }
      } finally {
        setReminderLoading(false);
      }
    }, 800);

    return () => clearTimeout(timer);
  }, [formTitle]); // eslint-disable-line react-hooks/exhaustive-deps

  const handleReminderChange = (text: string) => {
    setFormReminder(text);
    setReminderAutoGenerated(false); // user manually edited
  };

  const resetForm = () => {
    setFormTitle("");
    setFormDate(new Date());
    setFormReminder("");
    setFormReminderDays(0);
    setEditingId(null);
    setFormVisible(false);
    setReminderAutoGenerated(false);
  };

  const openAddForm = () => {
    resetForm();
    setFormVisible(true);
  };

  const openEditForm = (item: CustomAnniversary) => {
    setEditingId(item.id);
    setFormTitle(item.title);
    const [y, m, d] = item.date.split("-").map(Number);
    setFormDate(new Date(y, m - 1, d));
    setFormReminder(item.reminder || "");
    setFormReminderDays(item.reminderDaysBefore ?? 0);
    setReminderAutoGenerated(false);
    setFormVisible(true);
  };

  const handleSave = async () => {
    if (!formTitle.trim() || !userId) return;
    try {
      if (editingId) {
        await api.updateAnniversary(editingId, {
          userId,
          title: formTitle.trim(),
          date: formatDate(formDate),
          reminderDaysBefore: formReminderDays,
          reminderText: formReminder.trim() || undefined,
        });
      } else {
        await api.createAnniversary({
          userId,
          title: formTitle.trim(),
          date: formatDate(formDate),
          reminderDaysBefore: formReminderDays,
          reminderText: formReminder.trim() || undefined,
        });
      }
      await loadAnniversaries();
      resetForm();
    } catch (e: any) {
      Alert.alert(t("common.error"), e.message || "Save failed");
    }
  };

  const handleDelete = (id: string) => {
    Alert.alert(
      t("anniversary.deleteConfirm"),
      t("anniversary.deleteConfirmMessage"),
      [
        { text: t("common.cancel"), style: "cancel" },
        {
          text: t("anniversary.deleteBtn"),
          style: "destructive",
          onPress: async () => {
            try {
              await api.deleteAnniversary(id, { userId });
              await loadAnniversaries();
            } catch (e: any) {
              Alert.alert(t("common.error"), e.message || "Delete failed");
            }
          },
        },
      ]
    );
  };

  const sorted = [...anniversaries].sort(
    (a, b) => daysUntilNextOccurrence(a.date) - daysUntilNextOccurrence(b.date)
  );

  const handleTestNotification = async () => {
    const { status } = await Notifications.requestPermissionsAsync();
    if (status !== "granted") {
      Alert.alert(t("common.error"), t("anniversary.notificationPermission"));
      return;
    }
    // Use the nearest upcoming anniversary's real reminder content
    const nearest = sorted[0];
    const title = nearest?.title || t("anniversary.testNotificationTitle");
    const body = nearest?.reminder || t("anniversary.testNotificationBody");
    await Notifications.scheduleNotificationAsync({
      content: { title, body },
      trigger: null,
    });
  };

  // --- Render ---
  return (
    <SafeAreaView
      style={[styles.screen, { backgroundColor: theme.colorBackground }]}
    >
      <ScrollView stickyHeaderIndices={[0]}>
        {/* Header */}
        <View
          style={[
            styles.header,
            {
              backgroundColor: theme.colorBackground,
              borderBottomColor: theme.colorBorder,
            },
          ]}
        >
          <View style={styles.headerRow}>
            <TouchableOpacity onPress={onBack} style={styles.backBtn}>
              <ArrowLeft size={22} color={theme.colorForeground} />
            </TouchableOpacity>
            <Text style={[styles.headerTitle, { color: theme.colorForeground }]}>
              {t("anniversary.pageTitle")}
            </Text>
            {linkedUser ? (
              <TouchableOpacity onPress={openAddForm} style={styles.backBtn}>
                <Plus size={22} color={theme.colorForeground} />
              </TouchableOpacity>
            ) : (
              <View style={{ width: 30 }} />
            )}
          </View>
        </View>

        <View style={styles.content}>
          {!linkedUser ? (
            <View style={styles.emptyState}>
              <CalendarHeart size={40} color={theme.colorMutedForeground} />
              <Text style={[styles.emptyTitle, { color: theme.colorForeground }]}>
                {t("anniversary.emptyTitle")}
              </Text>
              <Text style={[styles.emptyDesc, { color: theme.colorMutedForeground }]}>
                {t("anniversary.notLinked")}
              </Text>
            </View>
          ) : (
          <>
          {/* Auto milestones */}
          {relationshipStartDate && (
            <View style={{ marginBottom: 20 }}>
              <Text
                style={[
                  styles.sectionLabel,
                  { color: theme.colorMutedForeground },
                ]}
              >
                {t("anniversary.autoMilestones")}
              </Text>
              <AutoMilestoneList
                startDate={relationshipStartDate}
                theme={theme}
                t={t}
              />
            </View>
          )}

          {/* Custom section */}
          <Text
            style={[
              styles.sectionLabel,
              { color: theme.colorMutedForeground },
            ]}
          >
            {t("anniversary.customTitle")}
          </Text>

          {loading && (
            <View style={{ alignItems: "center", paddingVertical: 40 }}>
              <ActivityIndicator size="large" color={theme.colorPrimary} />
            </View>
          )}

          {!loading && sorted.length === 0 && !formVisible && (
            <TouchableOpacity
              style={[
                styles.emptyCard,
                {
                  backgroundColor: theme.colorSecondary,
                  borderColor: theme.colorBorder,
                },
              ]}
              onPress={openAddForm}
              activeOpacity={0.7}
            >
              <CalendarHeart size={40} color={theme.colorMutedForeground} />
              <Text
                style={[styles.emptyTitle, { color: theme.colorForeground }]}
              >
                {t("anniversary.emptyTitle")}
              </Text>
              <Text
                style={[
                  styles.emptyDesc,
                  { color: theme.colorMutedForeground },
                ]}
              >
                {t("anniversary.emptyDesc")}
              </Text>
            </TouchableOpacity>
          )}

          {sorted.map((item) => (
            <AnniversaryItem
              key={item.id}
              item={item}
              onEdit={openEditForm}
              onDelete={handleDelete}
              theme={theme}
              t={t}
              language={language}
            />
          ))}

          {/* Add / Edit form */}
          {formVisible && (
            <View
              style={[
                styles.addForm,
                {
                  backgroundColor: theme.colorCard,
                  borderColor: theme.colorBorder,
                },
              ]}
            >
              <View style={styles.addFormHeader}>
                <Text
                  style={[
                    styles.addFormTitle,
                    { color: theme.colorForeground },
                  ]}
                >
                  {editingId
                    ? t("anniversary.editTitle")
                    : t("anniversary.addTitle")}
                </Text>
                <TouchableOpacity
                  onPress={resetForm}
                  style={styles.iconBtnSmall}
                >
                  <X size={18} color={theme.colorMutedForeground} />
                </TouchableOpacity>
              </View>

              {/* Name */}
              <Text
                style={[
                  styles.inputLabel,
                  { color: theme.colorMutedForeground },
                ]}
              >
                {t("anniversary.nameLabel")}
              </Text>
              <TextInput
                style={[
                  styles.input,
                  {
                    backgroundColor: theme.colorInputBackground,
                    color: theme.colorForeground,
                    borderColor: theme.colorBorder,
                  },
                ]}
                value={formTitle}
                onChangeText={handleTitleChange}
                placeholder={t("anniversary.namePlaceholder")}
                placeholderTextColor={theme.colorMutedForeground}
              />

              {/* Date */}
              <Text
                style={[
                  styles.inputLabel,
                  { color: theme.colorMutedForeground },
                ]}
              >
                {t("anniversary.dateLabel")}
              </Text>
              <TouchableOpacity
                style={[
                  styles.dateButton,
                  {
                    backgroundColor: theme.colorInputBackground,
                    borderColor: theme.colorBorder,
                  },
                ]}
                onPress={() => setShowDatePicker(true)}
              >
                <Text style={{ color: theme.colorForeground, fontSize: 15 }}>
                  {formatDate(formDate)}
                </Text>
              </TouchableOpacity>
              {showDatePicker && (
                <DateTimePicker
                  value={formDate}
                  mode="date"
                  display={Platform.OS === "ios" ? "spinner" : "default"}
                  onChange={(_, date) => {
                    if (Platform.OS !== "ios") setShowDatePicker(false);
                    if (date) setFormDate(date);
                  }}
                />
              )}
              {showDatePicker && Platform.OS === "ios" && (
                <TouchableOpacity
                  onPress={() => setShowDatePicker(false)}
                  style={[
                    styles.confirmDateBtn,
                    { backgroundColor: theme.colorPrimary },
                  ]}
                >
                  <Text
                    style={{
                      color: theme.colorPrimaryForeground,
                      fontWeight: "600",
                    }}
                  >
                    {t("anniversary.confirmDate")}
                  </Text>
                </TouchableOpacity>
              )}

              {/* Reminder text */}
              <Text
                style={[
                  styles.inputLabel,
                  { color: theme.colorMutedForeground },
                ]}
              >
                {t("anniversary.reminderLabel")}
              </Text>
              <TextInput
                style={[
                  styles.input,
                  {
                    backgroundColor: theme.colorInputBackground,
                    color: theme.colorForeground,
                    borderColor: theme.colorBorder,
                  },
                ]}
                value={formReminder}
                onChangeText={handleReminderChange}
                placeholder={t("anniversary.reminderPlaceholder")}
                placeholderTextColor={theme.colorMutedForeground}
              />

              {/* Reminder time */}
              <Text
                style={[
                  styles.inputLabel,
                  { color: theme.colorMutedForeground },
                ]}
              >
                {t("anniversary.reminderTimeLabel")}
              </Text>
              <View style={styles.reminderOptions}>
                {REMINDER_OPTIONS.map((opt) => {
                  const selected = formReminderDays === opt.value;
                  return (
                    <TouchableOpacity
                      key={opt.value}
                      style={[
                        styles.reminderChip,
                        {
                          backgroundColor: selected
                            ? theme.colorPrimary
                            : theme.colorInputBackground,
                          borderColor: selected
                            ? theme.colorPrimary
                            : theme.colorBorder,
                        },
                      ]}
                      onPress={() => setFormReminderDays(opt.value)}
                      activeOpacity={0.7}
                    >
                      <Text
                        style={[
                          styles.reminderChipText,
                          {
                            color: selected
                              ? theme.colorPrimaryForeground
                              : theme.colorForeground,
                          },
                        ]}
                      >
                        {t(opt.labelKey)}
                      </Text>
                    </TouchableOpacity>
                  );
                })}
              </View>

              {/* Save */}
              <TouchableOpacity
                style={[
                  styles.saveBtn,
                  {
                    backgroundColor: theme.colorPrimary,
                    opacity: formTitle.trim() ? 1 : 0.5,
                  },
                ]}
                onPress={handleSave}
                disabled={!formTitle.trim()}
                activeOpacity={0.7}
              >
                <Text
                  style={[
                    styles.saveBtnText,
                    { color: theme.colorPrimaryForeground },
                  ]}
                >
                  {editingId
                    ? t("anniversary.updateBtn")
                    : t("anniversary.saveBtn")}
                </Text>
              </TouchableOpacity>

              {/* Test notification */}
              <TouchableOpacity
                style={[
                  styles.testBtn,
                  { borderColor: theme.colorBorder },
                ]}
                onPress={handleTestNotification}
                activeOpacity={0.7}
              >
                <Bell size={16} color={theme.colorMutedForeground} />
                <Text
                  style={[
                    styles.testBtnText,
                    { color: theme.colorMutedForeground },
                  ]}
                >
                  {t("anniversary.testNotification")}
                </Text>
              </TouchableOpacity>
            </View>
          )}
          </>
          )}
        </View>
      </ScrollView>
    </SafeAreaView>
  );
}

