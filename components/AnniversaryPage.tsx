function AutoMilestoneList({
  startDate,
  theme,
  t,
}: {
  startDate: Date;
  theme: any;
  t: (key: string) => string;
}) {
  const today = new Date();
  const todayDate = new Date(
    today.getFullYear(),
    today.getMonth(),
    today.getDate()
  );
  const startUTC = Date.UTC(
    startDate.getFullYear(),
    startDate.getMonth(),
    startDate.getDate()
  );
  const nowUTC = Date.UTC(
    todayDate.getFullYear(),
    todayDate.getMonth(),
    todayDate.getDate()
  );
  const daysTogether = Math.max(
    0,
    Math.round((nowUTC - startUTC) / (1000 * 60 * 60 * 24))
  );

  const milestones: { label: string; daysUntil: number }[] = [];

  const next100 = (Math.floor(daysTogether / 100) + 1) * 100;
  milestones.push({
    label: t("anniversary.day100").replace("{count}", String(next100)),
    daysUntil: next100 - daysTogether,
  });

  const startDay = startDate.getDate();
  let monthCandidate = new Date(
    todayDate.getFullYear(),
    todayDate.getMonth(),
    startDay
  );
  if (monthCandidate.getDate() !== startDay) {
    monthCandidate = new Date(
      todayDate.getFullYear(),
      todayDate.getMonth() + 1,
      0
    );
  }
  if (monthCandidate <= todayDate) {
    monthCandidate = new Date(
      todayDate.getFullYear(),
      todayDate.getMonth() + 1,
      startDay
    );
    if (monthCandidate.getDate() !== startDay) {
      monthCandidate = new Date(
        todayDate.getFullYear(),
        todayDate.getMonth() + 2,
        0
      );
    }
  }
  const monthsDiff =
    (monthCandidate.getFullYear() - startDate.getFullYear()) * 12 +
    (monthCandidate.getMonth() - startDate.getMonth());
  if (monthsDiff > 0 && monthsDiff % 12 !== 0) {
    milestones.push({
      label: t("anniversary.monthlyAnniversary").replace(
        "{count}",
        String(monthsDiff)
      ),
      daysUntil: Math.round(
        (monthCandidate.getTime() - todayDate.getTime()) /
          (1000 * 60 * 60 * 24)
      ),
    });
  }

  let nextYear = new Date(
    todayDate.getFullYear(),
    startDate.getMonth(),
    startDate.getDate()
  );
  if (nextYear <= todayDate) {
    nextYear = new Date(
      todayDate.getFullYear() + 1,
      startDate.getMonth(),
      startDate.getDate()
    );
  }
  const yearsDiff = nextYear.getFullYear() - startDate.getFullYear();
  if (yearsDiff > 0) {
    milestones.push({
      label: t("anniversary.yearlyAnniversary").replace(
        "{count}",
        String(yearsDiff)
      ),
      daysUntil: Math.round(
        (nextYear.getTime() - todayDate.getTime()) / (1000 * 60 * 60 * 24)
      ),
    });
  }

  milestones.sort((a, b) => a.daysUntil - b.daysUntil);

  return (
    <>
      {milestones.map((m, i) => {
        const { text } = formatCountdown(m.daysUntil, t);
        return (
          <View
            key={i}
            style={[
              styles.milestoneRow,
              { backgroundColor: theme.colorSecondary },
            ]}
          >
            <Text
              style={[
                styles.milestoneLabel,
                { color: theme.colorForeground },
              ]}
            >
              {m.label}
            </Text>
            <Text
              style={[
                styles.milestoneCountdown,
                { color: theme.colorMutedForeground },
              ]}
            >
              {text}
            </Text>
          </View>
        );
      })}
    </>
  );
}

